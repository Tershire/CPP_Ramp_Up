// HEADER FILE ////////////////////////////////////////////////////////////////
#include <iostream>


// MAIN ///////////////////////////////////////////////////////////////////////
int main()
{
	using namespace std;

	// Pointer ================================================================
	int a = 7;
	int* p_a = &a;

	cout << "______________________________________________________________\n"
		 << "value stored @ " << p_a << " is " << *p_a << endl;

	// Reference ==============================================================
	int& b = a;

	cout << "______________________________________________________________\n"
		 << "value stored @ " << &b << " is " << b << endl;

	return 0;
}
